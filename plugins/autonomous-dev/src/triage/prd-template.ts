// Template builder
// ---------------------------------------------------------------------------

/**
 * Builds the complete PRD Markdown content from observation data and
 * LLM-generated content.
 *
 * The output is compatible with the existing autonomous development
 * pipeline format (TDD-001). Frontmatter includes:
 *   title, version, date, author, status, source, observation_id, severity, service.
 *
 * Body includes:
 *   - Problem Statement with quantitative metrics
 *   - Evidence copied from observation
 *   - Constraints referencing the observation
 *   - Success Criteria table
 *   - Scope
 *
 * @param prdId     The generated PRD ID (e.g., "PRD-OBS-20260408-143022-a7f3")
 * @param observation Structured observation data
 * @param llmContent  LLM-generated PRD sections
 * @returns Complete Markdown string with frontmatter
 */
export function buildPrdContent(
  prdId: string,
  observation: ObservationData,
  llmContent: LlmPrdContent,
): string {
  const frontmatterFields: Record<string, string | number | boolean | null> = {
    title: `Fix: ${llmContent.title}`,
    version: PRD_VERSION,
    date: new Date().toISOString().split('T')[0],
    author: PRD_AUTHOR,
    status: PRD_STATUS,
    source: PRD_SOURCE,
    observation_id: observation.id,
    severity: observation.severity,
    service: observation.service,
  };

  const frontmatter = serializePrdFrontmatter(frontmatterFields);

  const body = `
# ${llmContent.title}

## Problem Statement

${llmContent.problemStatement}

## Evidence

${observation.evidence}

## Constraints

- Fix must address the root cause identified in observation ${observation.id}
- Target metric: ${observation.targetMetric} must improve by at least 10% post-deployment
- Service: ${observation.service}, Repo: ${observation.repo}

## Success Criteria

| Metric | Current (broken) | Target (fixed) | Measurement |
|--------|-----------------|----------------|-------------|
| ${observation.targetMetric} | ${observation.currentValue} | ${observation.baselineValue} | Prometheus query post-deploy |

## Scope

${llmContent.scope}
`;

  return frontmatter + body;
}

// ---------------------------------------------------------------------------
// Prompt builder
// ---------------------------------------------------------------------------

/**
 * Builds the LLM prompt for PRD generation by interpolating observation
 * data into the prompt template.
 *
 * @param observationFullText The full observation report markdown
 * @param service             Service name
 * @param repo                Repository name
 * @param severity            Severity/criticality level
 * @param previousSummary     Summary of previous observations (may be empty)
 * @returns Interpolated prompt string
 */
export function buildPrdPrompt(
  observationFullText: string,
  service: string,
  repo: string,
  severity: string,
  previousSummary: string,
): string {
  return PRD_GENERATION_PROMPT
    .replace('{observation_report_full_text}', observationFullText)
    .replace('{service}', service)
    .replace('{repo}', repo)
    .replace('{criticality}', severity)
    .replace('{previous_observations_summary}', previousSummary || 'None');
}
